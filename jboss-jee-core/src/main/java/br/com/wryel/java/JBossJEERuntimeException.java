package br.com.wryel.java;

public class JBossJEERuntimeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6495946675063295889L;

	public JBossJEERuntimeException() {
		super();
	}

	public JBossJEERuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public JBossJEERuntimeException(String message) {
		super(message);
	}

	public JBossJEERuntimeException(Throwable cause) {
		super(cause);
	}
}
